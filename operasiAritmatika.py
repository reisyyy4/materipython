# Operasi Aritmatika
x = 4
y = 2

# Prioritas Operasi Aritmatika
'''
    Mengutamakan: 
    1. Bilangan yang berada di dalam kurung ()
    2. Eksponen 
    3. Perkalian, pembagian, modulus, floor division
    4. Penjumlahan dan pengurangan
'''

# Operasi Penjumlahan
hasil_penjumlahan = x + y
print("Hasil dari penjumlahan variabel x dan y :", hasil_penjumlahan)

# Operasi Pengurangan
hasil_pengurangan = x - y
print("Hasil dari pengurangan variabel x dan y :", hasil_pengurangan)

# Operasi Perkalian
hasil_perkalian = x * y
print("Hasil dari perkalian variabel x dan y :", hasil_perkalian)

# Operasi Pembagian
hasil_pembagian = x / y
print("Hasil dari penmbagian variabel x dan y :", hasil_pembagian)

# Operasi Eksponen (Pangkat)
hasil_eksponen = x ** y
print("Hasil dari variabel x dipangkatkan variabel y :", hasil_eksponen)

# Operasi Modulus (Sisa Pembagian)
hasil_modulus = x % y
print("Sisa pembagian dari variabel x dibagi dengan variabel y :", hasil_modulus)

# Operasi Floor Division (Hasil Pembagian yang dibulatkan ke bawah)
hasil_floorDivision = x // y
print("Hasil pembagian dari variabel x dan variabel y :", hasil_floorDivision)
