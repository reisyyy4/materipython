# Tipe data adalah jenis informasi yang bisa disimpan dan diolah oleh komputer
# Berikut type data standar yang ada di python

# 1. Integer adalah bilangan bulat yang tidak mempunyai angka desimal
a = 10
print("Nilai dari variabel a adalah", a, "yang memiliki tipe data ", type(a))

# 2. Float adalah bilangan desimal atau bilangan pecahan
b = 7.5
print("Nilai dari variabel b adalah", b, "yang memiliki tipe data ", type(b))

# 3. String adalah kumpulan karakter
c = "Okan"
print("Nilai dari variabel c adalah", c, "yang memiliki tipe data ", type(c))

# 4. Boolean adalah biner atau hanya ada true/false
d = True
print("Nilai dari variabel d adalah", d, "yang memiliki tipe data ", type(d))
